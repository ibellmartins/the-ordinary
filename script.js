function btn1on(){
    var changeBtn = document.getElementById('btn1');
    document.getElementById('txt1').style.visibility = "visible";
    document.getElementById('price1').style.visibility = "visible";
     changeBtn.style.backgroundColor = 'black';
     changeBtn.style.color = "white";
}

function btn1out(){
    var changeBtn = document.getElementById('btn1');
    changeBtn.style.backgroundColor = 'white';
    changeBtn.style.color = "black";
    txt1.style.visibility = 'hidden';
    price1.style.visibility = "hidden";
}

